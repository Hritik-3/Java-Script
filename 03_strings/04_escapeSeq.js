
//Escape Sequence Characters
//this is used to print the special characters
// \"	Double quote inside a string
// \'	Single quote inside a string
// \\	Backslash (\)
// \n	New line
// \t	Tab (horizontal space)
// \r	Carriage return
// \b	Backspace
// \f	Form feed

let w = 'Can\'nt'
console.log(w)
let s = 'Can\"nt'
console.log(s)
//\n is used for new line
let f = 'Can\n\'nt'
console.log(f)
//\t is used for tab or space
let g = 'Can\t\'nt'
console.log(g)
//\r is used for carriage return
let h = "Hello\rWorld";
console.log(h);
